package basics;

public class JavaPlatform 
{
/*
 * A platform is a hardware or software environment in which a program runs.
 * Java is a software only Platform. It consists of
 * 
 * It consists of three components mainly:
 * 
 * 1. JDK
 * 2. JRE
 * 3. JVM
 * 
 * JDK:
 * 
 * The Java Development Kit (JDK) is a software development environment used for 
 * developing Java applications and applets. It includes the Java Runtime Environment (JRE),
 * an interpreter/loader (Java), a compiler (javac), an archiver (jar), a documentation generator (Javadoc) 
 * and other tools needed in Java development.
 * 
 * JRE:
 * 
 * JRE stands for “Java Runtime Environment” and may also be written as “Java RTE.” 
 * The Java Runtime Environment provides the minimum requirements for executing a Java application; 
 * it consists of the Java Virtual Machine (JVM), core classes, and supporting files. Core classes include
 * JDBC, JNDI etc.
 * 
 * JVM
 * 
 * JVM is -
 * 
 * 1. A specification where working of Java Virtual Machine is specified. But implementation provider is independent to choose the algorithm. 
 *    Its implementation has been provided by Sun and other companies.
 * 2. An implementation is a computer program that meets the requirements of the JVM specification
 * 3. Runtime Instance Whenever you write java command on the command prompt to run the java class, an instance of JVM is created.
 * 
 * Difference b/w JDK, JRE and JVM
 * 
 * JDK – Java Development Kit (in short JDK) is Kit which provides the environment to develop and execute(run) the Java program. JDK is a kit(or package) which includes two things
 * 		Development Tools(to provide an environment to develop your java programs)
 * 		JRE (to execute your java program).
 * 
 * Note : JDK is only used by Java Developers.
 *
 * JRE – Java Runtime Environment (to say JRE) is an installation package which provides environment to only run(not develop) 
 * 		 the java program(or application)onto your machine. JRE is only used by them who only wants to run the Java Programs i.e. end users of your system.
 * 
 * JVM – Java Virtual machine(JVM) is a very important part of both JDK and JRE because it is contained or inbuilt in both. Whatever Java program you run 
 * 		 using JRE or JDK goes into JVM and JVM is responsible for executing the java program line by line hence it is also known as interpreter.
 * 
 */
}
